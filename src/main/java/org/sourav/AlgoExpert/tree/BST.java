package org.sourav.AlgoExpert.tree;

public class BST {
    public int value;
    public BST left;
    public BST right;

    public BST() {
        this.value = 0;
        this.left = null;
        this.right = null;
    }

    public BST(int value) {
        this.value = value;
        this.left = null;
        this.right = null;
    }

}
